function startQuiz() {
  current = 0;
  scores = { control: 0, approval: 0, defense: 0, attack: 0 };
  showScreen(quiz);
  renderQuestion();
}

function showScreen(target) {
  [intro, quiz, result].forEach((screen) => screen.classList.remove("active"));
  target.classList.add("active");
  scrollTo({ top: 0, behavior: "smooth" });
}

function renderQuestion() {
  const q = questions[current];
  progressText.textContent = `Q${current + 1} / ${questions.length}`;
  progressBar.style.width = `${(current / questions.length) * 100}%`;
  questionText.textContent = q.text;
  answers.innerHTML = "";

  q.answers.forEach(([axis, label]) => {
    const button = document.createElement("button");
    button.className = "answer-btn";
    button.type = "button";
    button.textContent = label;
    button.addEventListener("click", () => {
      scores[axis] += 1;
      current += 1;
      if (current >= questions.length) {
        progressBar.style.width = "100%";
        showResult();
      } else {
        renderQuestion();
      }
    });
    answers.appendChild(button);
  });
}

function showResult() {
  const sorted = axes
    .map((axis, index) => ({ axis, score: scores[axis], index }))
    .sort((a, b) => b.score - a.score || a.index - b.index);

  const primary = sorted[0].axis;
  const secondary = sorted[1].score === sorted[0].score ? sorted[0].axis : sorted[1].axis;
  const resultIndex = comboMap[`${primary}-${secondary}`] ?? 0;
  currentResult = results[resultIndex];

  document.getElementById("resultName").textContent = currentResult.name;
  document.getElementById("resultCatch").textContent = currentResult.catch;
  document.getElementById("resultImage").src = currentResult.image;
  document.getElementById("resultImage").alt = currentResult.name;
  document.getElementById("resultBody").textContent = currentResult.body;
  document.getElementById("resultView").textContent = currentResult.view;
  document.getElementById("resultRisk").textContent = currentResult.risk;
  document.getElementById("resultTip").textContent = currentResult.tip;
  renderLongReading(currentResult.name);
  renderCompatibility(currentResult.name);
  renderDeepDive(currentResult.name);
  showScreen(result);
}

